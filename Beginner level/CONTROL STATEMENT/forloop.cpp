/*c++ program which involves concepts of for loop in c++
this is a c++ program which asks user to input how many number want to add
then ask him to enter and display their sum */
#include<iostream>
using namespace std;
int main()
{
  int sum =0;
  int n;
  int value;
  cout<<"enter the number you want to add numbers :"<<endl;
  cin>>n;
  cout<<"enter"<<" " <<n<<" "<<"integers"<<endl;
  for(int x = 1; x<=n ;x++)
  {
      cin>>value;
      sum = sum + value;
  }
  cout<<"sum of the entered integers are :"<<sum<<endl;
  return 0;
}

